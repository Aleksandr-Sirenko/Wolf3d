#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*dup;
	int		len;

	i = 0;
	len = 0;
	while (s1[i])
	{
		len++;
		i++;
	}
	dup = (char*)malloc(sizeof(char) * (len + 1));
	if (!dup)
		return (NULL);
	i = 0;
	while (i < len)
	{
		dup[i] = s1[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
